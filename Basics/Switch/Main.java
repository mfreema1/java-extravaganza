public class Main {

    public static void main(String[] args) {

        int code = 5;
        switch(code) {
            case 1:
                System.out.println("It is one");
                break;
            case 3:
                System.out.println("It is three");
                break;
            case 5:
                System.out.println("It is five");
                break;
            default:
                System.out.println("I don't know what it is");
                break;
        }
    }
}